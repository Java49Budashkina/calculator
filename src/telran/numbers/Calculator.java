package telran.numbers;

public class Calculator {
	static public int multiply(int val1, int val2) {
		return val1 * val2;
	}
	static public int sum(int val1, int val2) {
		return val1 + val2;
	}
	static public int subtract(int val1, int val2) {
		return val1 - val2;
	}
	static public int devide(int val1, int val2) {
		return val1 / val2;
	}
	static public int remainder(int val1, int val2) {
		return val1 % val2;
	}
}
